// Template for Tier 3
function genTier3(c) {
  const [slug,nk,nc,ne,court,dates,title,faction,stats,tier,temperament,values,decision,tone,combat,weakness,rels] = c;
  const total = stats.reduce((a,b)=>a+b,0);
  let relTable = '';
  if (rels.length > 0) {
    relTable = `\n| 대상 | 관계 | 강도 | 설명 |\n|---|---|---|---|\n`;
    for (const [name,rel,str,desc] of rels) {
      relTable += `| ${name} | ${rel} | ${str} | ${desc} |\n`;
    }
  }

  return `# ${nk} (${nc}, ${ne})

> 자: ${court} | ${dates} | ${title}

## 능력치

| 통솔 | 무력 | 지력 | 정치 | 매력 | 총합 |
|---|---|---|---|---|---|
| ${stats[0]} | ${stats[1]} | ${stats[2]} | ${stats[3]} | ${stats[4]} | ${total} |

## 성격 프로파일

### 기질
- ${temperament}

### 가치관 우선순위
1. ${values[0]}
2. ${values[1]}
3. ${values[2]}

### 의사결정 패턴
- ${decision}

### 대화 톤
- ${tone}

## 행동 근거 (AI 시뮬레이션 지침)

- 전투/역할: ${combat}
- 약점: ${weakness}

## 핵심 관계
${relTable}
## 메타 정보

- **생산 방식**: Tier ${tier} (AI 생성 — Claude Opus, 간결 포맷)
- **소스**: 14소스 퓨전 soul-data
`;
}

let created = 0;
let skipped = 0;
for (const c of characters) {
  const slug = c[0];
  const outPath = join(CHARS_DIR, `${slug}.soul.md`);
  if (existsSync(outPath)) { skipped++; continue; }
  const content = genTier3(c);
  writeFileSync(outPath, content, 'utf-8');
  created++;
}

